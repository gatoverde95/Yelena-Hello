# Default language fallback
default_language = 'es'

class Translator:
    def __init__(self, language='es'):
        self.language = language if language in translations else default_language
        
    def set_language(self, language):
        if language in translations:
            self.language = language
            return True
        return False
        
    def get(self, key, default=None):
        """Get translated string for key"""
        if key == 'slider_phrases':
            return translations.get(self.language, {}).get(key, 
                   translations[default_language].get(key, []))
        
        return translations.get(self.language, {}).get(key, 
               translations[default_language].get(key, default or key))
               
    def get_available_languages(self):
        """Return a list of available language codes"""
        return list(translations.keys())